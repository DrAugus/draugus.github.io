// 获取中国地区的 CardLink
const getChinaCardLink = (): CardLink[] => {
    return getSortedProvince().reduce((cardLink: CardLink[], v: Province) => {
        const one: CardLink = {
            icon: { svg: generateSvg(v.abbreviation) },
            title: v.name,
            desc: '',
            link: `${PREFIX_TRIP_CHINA}/${v.pinyin}`
        };
        if (v.name !== v.capital) {
            one.link += '/';
        }
        cardLink.push(one);
        if (v.name === '广东') {
            cardLink.push(...HKMA);
        }
        return cardLink;
    }, []);
};

